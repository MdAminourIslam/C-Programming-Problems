#include<stdio.h>
int main()
{
    int n;
    scanf("%d",&n);
    switch(n)
    {
    case 1:

        printf("the value is one");
        break;

    case 2:

        printf("the value is two");
        break;

    case 3:

        printf("the value is three");
        break;

    case 4:

        printf("the value is four");
        break;

    case 5:

        printf("the value is five");
        break;

    default :

        printf("the value is six");
        break;

    }
    return 0;
}
